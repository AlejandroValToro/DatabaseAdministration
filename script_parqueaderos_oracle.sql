-- --------------------------------------------------------------------------
-- CONSULTA 1: Implementa 4 JOINS o mas (usa 5 INNER JOINs)
-- --------------------------------------------------------------------------
-- Enunciado: Listar el nombre del propietario, la placa del vehiculo, el
-- numero de celda, el tamano de la celda, el nombre del parqueadero, el
-- nombre del administrador y la descripcion de cada objeto robado, para
-- todas las denuncias registradas.
-- --------------------------------------------------------------------------
SELECT p.nombre              AS propietario,
       v.placa               AS placa_vehiculo,
       c.numero_celda        AS celda,
       c.tamano              AS tamano_celda,
       pq.nombre             AS parqueadero,
       a.nombre              AS administrador,
       o.descripcion         AS objeto_robado,
       o.valor               AS valor_objeto,
       o.marca               AS marca_objeto,
       d.fecha_denuncia
FROM   DENUNCIA d
       INNER JOIN PROPIETARIO p     ON d.id_propietario = p.id_propietario
       INNER JOIN VEHICULO v        ON d.placa = v.placa
       INNER JOIN CELDA c           ON d.id_celda = c.id_celda
       INNER JOIN PARQUEADERO pq    ON c.id_parqueadero = pq.id_parqueadero
       INNER JOIN ADMINISTRADOR a   ON pq.id_administrador = a.id_administrador
       INNER JOIN OBJETO_ROBADO o   ON o.numero_denuncia = d.numero_denuncia
ORDER BY d.fecha_denuncia, o.descripcion;

-- --------------------------------------------------------------------------
-- CONSULTA 2: Implementa LEFT JOIN
-- --------------------------------------------------------------------------
-- Enunciado: Listar todos los parqueaderos junto con sus administradores
-- y las celdas que poseen, mostrando tambien aquellas celdas que NO han
-- tenido ninguna denuncia asociada (resultado no vacio gracias a las
-- celdas del Parqueadero Laureles Park).
-- --------------------------------------------------------------------------
SELECT pq.nombre             AS parqueadero,
       a.nombre              AS administrador,
       c.numero_celda        AS celda,
       c.tamano              AS tamano_celda,
       d.numero_denuncia,
       d.fecha_denuncia,
       d.tipo_denuncia
FROM   PARQUEADERO pq
       INNER JOIN ADMINISTRADOR a   ON pq.id_administrador = a.id_administrador
       INNER JOIN CELDA c           ON c.id_parqueadero = pq.id_parqueadero
       LEFT  JOIN DENUNCIA d        ON d.id_celda = c.id_celda
ORDER BY pq.nombre, c.numero_celda;

-- --------------------------------------------------------------------------
-- CONSULTA 3: Operaciones entre conjuntos (MINUS)
-- --------------------------------------------------------------------------
-- Enunciado: Obtener los propietarios que han presentado al menos una
-- denuncia de tipo PENAL pero que NO han presentado ninguna denuncia
-- de tipo INFORMAL.
-- --------------------------------------------------------------------------
SELECT p.id_propietario,
       p.nombre
FROM   PROPIETARIO p
       INNER JOIN DENUNCIA d    ON d.id_propietario = p.id_propietario
       INNER JOIN DENUNCIA_PENAL dp ON dp.numero_denuncia = d.numero_denuncia
MINUS
SELECT p.id_propietario,
       p.nombre
FROM   PROPIETARIO p
       INNER JOIN DENUNCIA d    ON d.id_propietario = p.id_propietario
       INNER JOIN DENUNCIA_INFORMAL di ON di.numero_denuncia = d.numero_denuncia;

-- --------------------------------------------------------------------------
-- CONSULTA 4: Subconsultas
-- --------------------------------------------------------------------------
-- Enunciado: Listar los vehiculos cuyo valor total de objetos robados
-- (sumando todas sus denuncias) supera el promedio general del valor
-- total por denuncia.
-- --------------------------------------------------------------------------
SELECT v.placa,
       v.marca,
       v.modelo,
       (SELECT SUM(o.valor)
        FROM   OBJETO_ROBADO o
               INNER JOIN DENUNCIA d ON o.numero_denuncia = d.numero_denuncia
        WHERE  d.placa = v.placa) AS valor_total_robado
FROM   VEHICULO v
WHERE  v.placa IN (
           SELECT d.placa
           FROM   DENUNCIA d
                  INNER JOIN OBJETO_ROBADO o ON o.numero_denuncia = d.numero_denuncia
           GROUP BY d.placa
           HAVING SUM(o.valor) > (
                      SELECT AVG(total_por_denuncia)
                      FROM   (SELECT SUM(o2.valor) AS total_por_denuncia
                              FROM   OBJETO_ROBADO o2
                              GROUP BY o2.numero_denuncia)
                  )
       )
ORDER BY valor_total_robado DESC;

