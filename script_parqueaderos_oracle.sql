-- --------------------------------------------------------------------------
-- FUNCION: fn_valor_total_denuncia
-- --------------------------------------------------------------------------
-- Enunciado: Funcion que recibe el numero de una denuncia y retorna el
-- valor total de todos los objetos robados registrados en dicha denuncia.
-- Si la denuncia no tiene objetos o no existe, retorna 0.
-- --------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION fn_valor_total_denuncia(
    p_numero_denuncia IN NUMBER
) RETURN NUMBER
IS
    v_total NUMBER := 0;
BEGIN
    SELECT NVL(SUM(valor), 0)
    INTO   v_total
    FROM   OBJETO_ROBADO
    WHERE  numero_denuncia = p_numero_denuncia;

    RETURN v_total;
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RETURN 0;
    WHEN OTHERS THEN
        RETURN -1;
END fn_valor_total_denuncia;
/

-- --------------------------------------------------------------------------
-- TRIGGER: trg_log_cambio_tipo_denuncia
-- --------------------------------------------------------------------------
-- Enunciado: Trigger que se dispara automaticamente cada vez que se
-- actualiza el tipo de una denuncia (campo tipo_denuncia en la tabla
-- DENUNCIA). Registra en la tabla LOG_CAMBIOS_DENUNCIA el numero de
-- denuncia, el tipo anterior, el nuevo tipo, la fecha del cambio y
-- el usuario que realizo la operacion.
-- --------------------------------------------------------------------------
CREATE OR REPLACE TRIGGER trg_log_cambio_tipo_denuncia
AFTER UPDATE OF tipo_denuncia ON DENUNCIA
FOR EACH ROW
BEGIN
    INSERT INTO LOG_CAMBIOS_DENUNCIA (
        numero_denuncia,
        tipo_anterior,
        tipo_nuevo,
        fecha_cambio,
        usuario
    ) VALUES (
        :OLD.numero_denuncia,
        :OLD.tipo_denuncia,
        :NEW.tipo_denuncia,
        SYSDATE,
        USER
    );
END trg_log_cambio_tipo_denuncia;
/

-- --------------------------------------------------------------------------
-- PROCEDIMIENTO ALMACENADO: sp_escalar_denuncias
-- --------------------------------------------------------------------------
-- Enunciado: Procedimiento que revisa todas las denuncias informales y
-- las escala automaticamente a denuncias penales cuando el valor total
-- de los objetos robados supera un umbral dado (en pesos colombianos).
-- Este proceso es util para el negocio porque permite a la Policia
-- identificar robos significativos que fueron reportados informalmente
-- y que ameritan investigacion judicial.
--
-- El procedimiento:
--   1. Usa un CURSOR para recorrer las denuncias informales.
--   2. Invoca la FUNCION fn_valor_total_denuncia para obtener el valor.
--   3. Si el valor supera el umbral:
--      - Actualiza el tipo de denuncia a 'PENAL' (dispara el TRIGGER).
--      - Inserta el registro en DENUNCIA_PENAL con juzgado 0 (pendiente).
--      - Elimina el registro de DENUNCIA_INFORMAL.
--   4. Maneja CONTROL DE ERRORES con SAVEPOINT por cada denuncia.
--   5. Garantiza ATOMICIDAD: si falla una denuncia individual, se revierte
--      solo esa y se continua con las demas.
-- --------------------------------------------------------------------------
CREATE OR REPLACE PROCEDURE sp_escalar_denuncias(
    p_umbral IN NUMBER
)
IS
    -- Cursor que recorre todas las denuncias informales
    CURSOR c_informales IS
        SELECT d.numero_denuncia,
               d.fecha_denuncia,
               d.placa,
               di.hora_denuncia
        FROM   DENUNCIA d
               INNER JOIN DENUNCIA_INFORMAL di ON d.numero_denuncia = di.numero_denuncia
        WHERE  d.tipo_denuncia = 'INFORMAL';

    v_valor_total   NUMBER;
    v_contador      NUMBER := 0;
    v_errores       NUMBER := 0;
BEGIN
    DBMS_OUTPUT.PUT_LINE('==============================================');
    DBMS_OUTPUT.PUT_LINE('INICIO DEL PROCESO DE ESCALAMIENTO');
    DBMS_OUTPUT.PUT_LINE('Umbral configurado: $' || TO_CHAR(p_umbral, '999,999,999'));
    DBMS_OUTPUT.PUT_LINE('Fecha de ejecucion: ' || TO_CHAR(SYSDATE, 'YYYY-MM-DD HH24:MI:SS'));
    DBMS_OUTPUT.PUT_LINE('==============================================');

    FOR rec IN c_informales LOOP
        -- Invocar la funcion para obtener el valor total de la denuncia
        v_valor_total := fn_valor_total_denuncia(rec.numero_denuncia);

        DBMS_OUTPUT.PUT_LINE('Denuncia #' || rec.numero_denuncia ||
                             ' | Placa: ' || rec.placa ||
                             ' | Valor total: $' || TO_CHAR(v_valor_total, '999,999,999'));

        IF v_valor_total > p_umbral THEN
            -- Crear punto de salvaguarda para atomicidad individual
            SAVEPOINT sp_denuncia_individual;

            BEGIN
                -- Paso 1: Actualizar tipo de denuncia (esto dispara el trigger)
                UPDATE DENUNCIA
                SET    tipo_denuncia = 'PENAL'
                WHERE  numero_denuncia = rec.numero_denuncia;

                -- Paso 2: Insertar en tabla de denuncias penales
                -- Juzgado 0 indica "pendiente de asignacion"
                INSERT INTO DENUNCIA_PENAL (numero_denuncia, numero_juzgado)
                VALUES (rec.numero_denuncia, 0);

                -- Paso 3: Eliminar de denuncias informales
                DELETE FROM DENUNCIA_INFORMAL
                WHERE  numero_denuncia = rec.numero_denuncia;

                v_contador := v_contador + 1;
                DBMS_OUTPUT.PUT_LINE('  >> ESCALADA a PENAL exitosamente.');

            EXCEPTION
                WHEN OTHERS THEN
                    -- Revertir solo esta denuncia
                    ROLLBACK TO sp_denuncia_individual;
                    v_errores := v_errores + 1;
                    DBMS_OUTPUT.PUT_LINE('  >> ERROR al escalar: ' || SQLERRM);
            END;
        ELSE
            DBMS_OUTPUT.PUT_LINE('  >> No supera el umbral. Se mantiene INFORMAL.');
        END IF;
    END LOOP;

    -- Confirmar todos los cambios exitosos
    COMMIT;

    DBMS_OUTPUT.PUT_LINE('==============================================');
    DBMS_OUTPUT.PUT_LINE('RESUMEN DEL PROCESO');
    DBMS_OUTPUT.PUT_LINE('Denuncias escaladas: ' || v_contador);
    DBMS_OUTPUT.PUT_LINE('Errores encontrados: ' || v_errores);
    DBMS_OUTPUT.PUT_LINE('==============================================');

EXCEPTION
    WHEN OTHERS THEN
        -- Si ocurre un error general, revertir todo
        ROLLBACK;
        DBMS_OUTPUT.PUT_LINE('ERROR CRITICO en el procedimiento: ' || SQLERRM);
        RAISE;
END sp_escalar_denuncias;
/

-- --------------------------------------------------------------------------
-- EJECUCION DE PRUEBA DEL PROCEDIMIENTO ALMACENADO
-- --------------------------------------------------------------------------
-- Se establece el umbral en 300,000 pesos. Las denuncias informales cuyo
-- valor total de objetos robados supere este monto seran escaladas a penales.
--
-- Denuncias informales actuales:
--   Denuncia 2 (Diego, DEF456):  valor = 350,000  -> SUPERA umbral -> se escala
--   Denuncia 4 (Laura, ABC123):  valor = 350,000  -> SUPERA umbral -> se escala
--   Denuncia 6 (Sofia, MNO345):  valor = 180,000  -> NO supera     -> se mantiene
-- --------------------------------------------------------------------------
SET SERVEROUTPUT ON;

EXEC sp_escalar_denuncias(300000);

-- Verificar resultados de la ejecucion:
-- 1. Denuncias que ahora son penales
SELECT d.numero_denuncia, d.tipo_denuncia, dp.numero_juzgado
FROM   DENUNCIA d
       INNER JOIN DENUNCIA_PENAL dp ON d.numero_denuncia = dp.numero_denuncia
ORDER BY d.numero_denuncia;

-- 2. Denuncias que siguen siendo informales
SELECT d.numero_denuncia, d.tipo_denuncia, di.hora_denuncia
FROM   DENUNCIA d
       INNER JOIN DENUNCIA_INFORMAL di ON d.numero_denuncia = di.numero_denuncia
ORDER BY d.numero_denuncia;

-- 3. Log de cambios generados por el trigger
SELECT * FROM LOG_CAMBIOS_DENUNCIA ORDER BY id_log;

