-- Tabla: ADMINISTRADOR
-- Almacena los datos basicos de los administradores de parqueaderos
CREATE TABLE ADMINISTRADOR (
    id_administrador    NUMBER GENERATED BY DEFAULT AS IDENTITY,
    cedula              VARCHAR2(20)    NOT NULL,
    nombre              VARCHAR2(100)   NOT NULL,
    telefono            VARCHAR2(20),
    email               VARCHAR2(100),
    direccion           VARCHAR2(200),
    CONSTRAINT pk_administrador PRIMARY KEY (id_administrador),
    CONSTRAINT uk_admin_cedula  UNIQUE (cedula)
);

-- Tabla: PARQUEADERO
-- Almacena la informacion de cada parqueadero publico estudiado
CREATE TABLE PARQUEADERO (
    id_parqueadero      NUMBER GENERATED BY DEFAULT AS IDENTITY,
    nit                 VARCHAR2(20)    NOT NULL,
    nombre              VARCHAR2(100)   NOT NULL,
    direccion           VARCHAR2(200)   NOT NULL,
    telefono            VARCHAR2(20),
    id_administrador    NUMBER          NOT NULL,
    CONSTRAINT pk_parqueadero       PRIMARY KEY (id_parqueadero),
    CONSTRAINT uk_parq_nit          UNIQUE (nit),
    CONSTRAINT fk_parq_admin        FOREIGN KEY (id_administrador)
                                    REFERENCES ADMINISTRADOR(id_administrador)
);

-- Tabla: CELDA
-- Cada celda pertenece a un parqueadero y tiene un numero secuencial
CREATE TABLE CELDA (
    id_celda            NUMBER GENERATED BY DEFAULT AS IDENTITY,
    numero_celda        NUMBER          NOT NULL,
    tamano              VARCHAR2(20)    NOT NULL,
    id_parqueadero      NUMBER          NOT NULL,
    CONSTRAINT pk_celda             PRIMARY KEY (id_celda),
    CONSTRAINT uk_celda_parq_num    UNIQUE (id_parqueadero, numero_celda),
    CONSTRAINT fk_celda_parq        FOREIGN KEY (id_parqueadero)
                                    REFERENCES PARQUEADERO(id_parqueadero),
    CONSTRAINT ck_celda_tamano      CHECK (tamano IN ('PEQUENA', 'MEDIANA', 'GRANDE'))
);

-- Tabla: VEHICULO
-- Vehiculos involucrados en robos, identificados por su placa
CREATE TABLE VEHICULO (
    placa               VARCHAR2(10)    NOT NULL,
    marca               VARCHAR2(50)    NOT NULL,
    modelo              VARCHAR2(50)    NOT NULL,
    anio                NUMBER(4)       NOT NULL,
    color               VARCHAR2(30),
    tipo                VARCHAR2(30),
    CONSTRAINT pk_vehiculo          PRIMARY KEY (placa)
);

-- Tabla: PROPIETARIO
-- Propietarios de vehiculos que pueden presentar denuncias
CREATE TABLE PROPIETARIO (
    id_propietario      NUMBER GENERATED BY DEFAULT AS IDENTITY,
    cedula              VARCHAR2(20)    NOT NULL,
    nombre              VARCHAR2(100)   NOT NULL,
    telefono            VARCHAR2(20),
    email               VARCHAR2(100),
    direccion           VARCHAR2(200),
    CONSTRAINT pk_propietario       PRIMARY KEY (id_propietario),
    CONSTRAINT uk_prop_cedula       UNIQUE (cedula)
);

-- Tabla: VEHICULO_PROPIETARIO
-- Relacion N:M entre vehiculos y propietarios
CREATE TABLE VEHICULO_PROPIETARIO (
    placa               VARCHAR2(10)    NOT NULL,
    id_propietario      NUMBER          NOT NULL,
    CONSTRAINT pk_veh_prop          PRIMARY KEY (placa, id_propietario),
    CONSTRAINT fk_vp_vehiculo       FOREIGN KEY (placa)
                                    REFERENCES VEHICULO(placa),
    CONSTRAINT fk_vp_propietario    FOREIGN KEY (id_propietario)
                                    REFERENCES PROPIETARIO(id_propietario)
);

-- Tabla: DENUNCIA (Supertipo)
-- Formato de denuncia numerado de forma unica
CREATE TABLE DENUNCIA (
    numero_denuncia     NUMBER GENERATED BY DEFAULT AS IDENTITY,
    fecha_denuncia      DATE            NOT NULL,
    tipo_denuncia       VARCHAR2(10)    NOT NULL,
    id_propietario      NUMBER          NOT NULL,
    placa               VARCHAR2(10)    NOT NULL,
    id_celda            NUMBER          NOT NULL,
    CONSTRAINT pk_denuncia          PRIMARY KEY (numero_denuncia),
    CONSTRAINT fk_den_propietario   FOREIGN KEY (id_propietario)
                                    REFERENCES PROPIETARIO(id_propietario),
    CONSTRAINT fk_den_vehiculo      FOREIGN KEY (placa)
                                    REFERENCES VEHICULO(placa),
    CONSTRAINT fk_den_celda         FOREIGN KEY (id_celda)
                                    REFERENCES CELDA(id_celda),
    CONSTRAINT ck_den_tipo          CHECK (tipo_denuncia IN ('PENAL', 'INFORMAL'))
);

-- Tabla: DENUNCIA_PENAL (Subtipo)
-- Denuncias que se investigan judicialmente
CREATE TABLE DENUNCIA_PENAL (
    numero_denuncia     NUMBER          NOT NULL,
    numero_juzgado      NUMBER          NOT NULL,
    CONSTRAINT pk_den_penal         PRIMARY KEY (numero_denuncia),
    CONSTRAINT fk_dp_denuncia       FOREIGN KEY (numero_denuncia)
                                    REFERENCES DENUNCIA(numero_denuncia)
);

-- Tabla: DENUNCIA_INFORMAL (Subtipo)
-- Denuncias que solo registran el hecho
CREATE TABLE DENUNCIA_INFORMAL (
    numero_denuncia     NUMBER          NOT NULL,
    hora_denuncia       VARCHAR2(8)     NOT NULL,
    CONSTRAINT pk_den_informal      PRIMARY KEY (numero_denuncia),
    CONSTRAINT fk_di_denuncia       FOREIGN KEY (numero_denuncia)
                                    REFERENCES DENUNCIA(numero_denuncia)
);

-- Tabla: OBJETO_ROBADO
-- Objetos sustraidos registrados en cada denuncia
CREATE TABLE OBJETO_ROBADO (
    id_objeto           NUMBER GENERATED BY DEFAULT AS IDENTITY,
    descripcion         VARCHAR2(100)   NOT NULL,
    valor               NUMBER(12,2)    NOT NULL,
    marca               VARCHAR2(50),
    numero_denuncia     NUMBER          NOT NULL,
    CONSTRAINT pk_objeto            PRIMARY KEY (id_objeto),
    CONSTRAINT fk_obj_denuncia      FOREIGN KEY (numero_denuncia)
                                    REFERENCES DENUNCIA(numero_denuncia)
);

-- Tabla: LOG_CAMBIOS_DENUNCIA
-- Tabla de auditoria para registrar cambios de tipo en denuncias (usada por el trigger)
CREATE TABLE LOG_CAMBIOS_DENUNCIA (
    id_log              NUMBER GENERATED BY DEFAULT AS IDENTITY,
    numero_denuncia     NUMBER          NOT NULL,
    tipo_anterior       VARCHAR2(10),
    tipo_nuevo          VARCHAR2(10),
    fecha_cambio        DATE            DEFAULT SYSDATE,
    usuario             VARCHAR2(50),
    CONSTRAINT pk_log_cambios       PRIMARY KEY (id_log)
);

